let x = 10, y = 2;

let resultado = x + y;

console.log(`A Soma de ${x} e ${y} é ${resultado}.`);

resultado = x - y;
console.log(`A Subtração de ${x} e ${y} é ${resultado}.`);

resultado = x * y;
console.log(`A Multiplicação de ${x} e ${y} é ${resultado}.`);

resultado = x / y;
console.log(`A soma Divisão ${x} e ${y} é ${resultado}.`);

// Podemos também imprimir o resultado assim:
resultado = x + y;
let mensagem = "A soma de " + x + " e " + y + " é " + resultado;
